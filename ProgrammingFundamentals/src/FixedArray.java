
public class FixedArray {
	
	public static void main(String[] args) {
		
		double [] numbers = new double[5];
		
		numbers[0] = 2.5;
		numbers[1] = 4.8;
		numbers[2] = 9.07;
		numbers[3] = 12.55;
		numbers[4] = 55.7;
		
		
		for(int i=0; i < numbers.length; i++) {
			System.out.println(numbers[i]);
		}
		
		
		
	}

}
